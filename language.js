// Get current language from localStorage or default to English
let currentLang = localStorage.getItem('abook-lang') || 'en';

// Function to set language
function setLanguage(lang) {
    currentLang = lang;
    localStorage.setItem('abook-lang', lang);
    updateContent();
    updateToggleButton();
}

// Function to update all content
function updateContent() {
    const t = translations[currentLang];
    
    // Update all elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const keys = element.getAttribute('data-i18n').split('.');
        let value = t;
        
        for (const key of keys) {
            value = value[key];
        }
        
        if (value) {
            element.textContent = value;
        }
    });
}

// Function to update toggle button text
function updateToggleButton() {
    const toggleBtn = document.getElementById('lang-toggle');
    if (toggleBtn) {
        const span = toggleBtn.querySelector('span');
        if (span) {
            span.textContent = translations[currentLang].langToggle;
        } else {
            // Fallback if span doesn't exist
            toggleBtn.textContent = '游깷 ' + translations[currentLang].langToggle;
        }
    }
}

// Function to toggle language
function toggleLanguage() {
    const newLang = currentLang === 'en' ? 'me' : 'en';
    setLanguage(newLang);
}

// Initialize on page load
document.addEventListener('DOMContentLoaded', function() {
    updateContent();
    updateToggleButton();
    
    // Add click event to toggle button
    const toggleBtn = document.getElementById('lang-toggle');
    if (toggleBtn) {
        toggleBtn.addEventListener('click', toggleLanguage);
    }
});
